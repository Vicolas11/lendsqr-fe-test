export const duplicateArrElem = (x: number): string[] => {
  const userStatusArr = ["Active", "Inactive", "Blacklisted", "Pending"];
  const times = x / userStatusArr.length;
  return userStatusArr.reduce((res, cur) => {
    return res.concat(Array(times).fill(cur)).sort(() => Math.random() - 0.5);
  }, []);
};

